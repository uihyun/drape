/** Substring search across brand names. Returns at most `limit` brands
 *  sorted by match position (prefix matches first), with the brand name
 *  as the alphabetical tie-breaker. Empty query → empty result. Mirrors
 *  searchCities in data/cities.js. */
export function searchBrands(query, { limit = 8 } = {}) {
  const q = String(query || '').trim().toLowerCase();
  if (!q) return [];
  const scored = [];
  for (const b of BRANDS) {
    const idx = b.toLowerCase().indexOf(q);
    if (idx !== -1) scored.push({ b, idx });
  }
  scored.sort((a, z) => a.idx - z.idx || a.b.localeCompare(z.b));
  return scored.slice(0, limit).map(s => s.b);
}
